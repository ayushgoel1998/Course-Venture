
import { useState, useEffect, useContext } from 'react'
import axios from 'axios'
import { toast } from 'react-toastify'
import Link from 'next/link'
import { Context } from '../context'
import { useRouter } from 'next/router'

const Register = () => {

    // to save in states
    const [name, setName] = useState('ayush');
    const [email, setEmail] = useState('ayush@gmail.com ');
    const [password, setPassword] = useState('asdfasdf');

    const { state: { user } } = useContext(Context);
    const router = useRouter();

    useEffect(() => {
        if (user != null) router.push("/");
    }, [user])


    const handleSubmit = async (e) => {
        e.preventDefault();
        //console.table({ name, email, password });

        try {
            const { data } = await axios.post(`/api/register`, {
                name, email, password,
            });
            // console.log("Register response", data);
            toast.success("Registered Successfully.Please Login.")
        }
        catch (err) {
            toast.error(err.response.data);
        }
    };

    return (
        <>
            <h2 className="jumbotron text-center bg-primary square">Register</h2>

            <div className="container col-md-4 offset-md-4 pb-5">
                <form onSubmit={handleSubmit}>
                    <input type="text" className='form-control mb-4 p-4'
                        value={name}
                        onChange={(e) => setName(e.target.value)}
                        placeholder="Enter Name"
                        required
                    />

                    <input type="email" className='form-control mb-4 p-4'
                        value={email}
                        onChange={(e) => setEmail(e.target.value)}
                        placeholder="Enter Email"
                        required
                    />

                    <input type="password" className='form-control mb-4 p-4'
                        value={password}
                        onChange={(e) => setPassword(e.target.value)}
                        placeholder="Enter Password"
                        required
                    />

                    <button type="submit" className="btn btn-default btn-primary"> Submit </button>

                </form>
                <p className="text-center p-3">
                    Already Registered?{"  "}
                    <Link href="/login">
                        <a> Login</a>
                    </Link>
                </p>
            </div>
        </>
    )
}

export default Register;