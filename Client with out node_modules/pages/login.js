
import { useState, useContext, useEffect } from 'react'
import axios from 'axios'
import { toast } from 'react-toastify'
import Link from 'next/link'

import { Context } from '../context'
import { useRouter } from 'next/router'

const Login = () => {

    // to save in states
    const [email, setEmail] = useState('ayush@gmail.com ');
    const [password, setPassword] = useState('asdfasdf');

    const { state: { user }, dispatch } = useContext(Context);
    //  const { user } = state;
    const router = useRouter();

    useEffect(() => {
        if (user != null) router.push("/");
    }, [user]);


    //console.log("STATE", state);

    const handleSubmit = async (e) => {
        e.preventDefault();
        //console.table({ name, email, password });

        try {
            const { data } = await axios.post(`/api/login`, {
                email, password,
            });
            // console.log("Login response", data);
            dispatch({
                type: "LOGIN",
                payload: data,
            })
            // save in local stroage too
            window.localStorage.setItem('user', JSON.stringify(data));
            // redirect
            router.push("/user");
        }
        catch (err) {
            toast.error(err.response.data);
        }
    };

    return (
        <>
            <h2 className="jumbotron text-center bg-primary square">Login</h2>

            <div className="container col-md-4 offset-md-4 pb-5">
                <form onSubmit={handleSubmit}>

                    <input type="email" className='form-control mb-4 p-4'
                        value={email}
                        onChange={(e) => setEmail(e.target.value)}
                        placeholder="Enter Email"
                        required
                    />

                    <input type="password" className='form-control mb-4 p-4'
                        value={password}
                        onChange={(e) => setPassword(e.target.value)}
                        placeholder="Enter Password"
                        required
                    />

                    <button type="submit" className="btn btn-default btn-primary"> Submit </button>

                </form>
                <p className="text-center p-3">
                    Not A Registered User? {"  "}
                    <Link href="/register">
                        <a> Register </a>
                    </Link>
                </p>
            </div>
        </>
    )
}

export default Login;