// making user schema
import mongoose from "mongoose"
const { Schema } = mongoose;


const userSchema = new Schema({
    name: {
        type: String,
        trim: true, // to remove extra spaces 
        required: true,
    },
    email: {
        type: String,
        trim: true, // to remove extra spaces 
        required: true,
        unique: true,
    },
    password: {
        type: String,
        //trim:true, // to remove extra spaces 
        required: true,
        min: 6,
        max: 64,
    },
    picture: {
        type: String,
        default: "/avatar.png",
    },
    role: {
        type: [String],
        default: ["subscriber"],
        enum: ["subscriber", "Instructor", "Admin"],
    },
    // handling payments
    stripe_account_id: "",
    stripe_seller: {},
    stripeSession: {},
},
    { timestaps: true }
);

export default mongoose.model("User", userSchema);